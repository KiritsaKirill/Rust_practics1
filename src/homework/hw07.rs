// Функція invert_the_case приймає рядок і змінює регістр кожного символа:
// якщо символ у верхньому регістрі, то він перетворюється на нижній,
// а якщо в нижньому — то на верхній.
fn invert_the_case(s: String) -> String {
    s.chars()
        .map(|c| {
            if c.is_uppercase() {
                c.to_lowercase().to_string()  // Перетворення на нижній регістр
            } else {
                c.to_uppercase().to_string()  // Перетворення на верхній регістр
            }
        })
        .collect()  // Збираємо символи назад у рядок
}

